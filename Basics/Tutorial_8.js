// Comparsion between different datatypes

// Number in string vs Number
console.log("2">1)

// String text vs Number
console.log("Hello">2)

// null 
console.log(null>0)
console.log(null==0)
console.log(null>=0)

// undefined
console.log(undefined>0)
console.log(undefined==0)
console.log(undefined>=0)

// check (==)
console.log("2"==2) // -> true

// strict check (===)
console.log("2"===2) // -> false
